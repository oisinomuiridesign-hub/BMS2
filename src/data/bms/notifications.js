// ─── Helpers ────────────────────────────────────────────────────────────────

export function getUnreadCount(items = notifications) {
  return items.filter((n) => n.status === 'UNREAD' || n.status === 'PENDING').length;
}

export function getPendingActionCount(items = notifications) {
  return items.filter((n) => n.actionable && n.status === 'PENDING').length;
}
