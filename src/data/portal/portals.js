// Helper: look up a portal by email + password for login
export function findPortalByCredentials(email, password) {
  return portals.find(
    (p) => p.loginEmail.toLowerCase() === email.toLowerCase() && p.loginPassword === password
  ) || null;
}

// Helper: look up a portal by its ID
export function findPortalById(id) {
  return portals.find((p) => p.id === id) || null;
}
